// for..in
// 1. give you access to the index in the array
// 2. Gives you access to non-numeric keys of arrays
// 3. Ignores/skips the empty occurences in array

// for..of
// 1. give you access to the array element itself.
// 2. Ignores the non-numeric keys of arrays
// 3. Prints 'undefined' for the empty occurences of array

// forEach
// 1. give you access to the array element and index.
// 2. Ignores the non-numeric keys of arrays
// 3. Ignores/skips the empty occurences in array

// for loop
// 1. give you access to the index in the array
// 2. Ignores the non-numeric keys of arrays
// 3. Prints 'undefined' for the empty occurences of array
